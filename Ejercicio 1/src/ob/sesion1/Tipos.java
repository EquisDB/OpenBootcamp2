package ob.sesion1;

public class Tipos {

    public static void main(String[] args) {

        //Imprime todos los tipos de datos numericos enteros
        byte variable1 = 2;
        System.out.println(variable1);
        short variable2 = 9;
        System.out.println(variable2);
        int variable3 = 34;
        System.out.println(variable3);
        long variable4 = 344;
        System.out.println(variable4);

        //Imprime todos los tipos de datos numericos decimales
        float variable5 = 2.2f;
        System.out.println(variable5);
        double variable6 = 2.222d;
        System.out.println(variable6);

        //imprime todos los tipos de datos booleanos
        boolean variable7 = true;
        System.out.println(variable7);
        boolean variable8 = false;
        System.out.println(variable8);

        //Imprime todos los tipos de datos de texto
        char variable9 = 'a';
        System.out.println(variable9);
        String variable10 = "Esto es un String";
        System.out.println(variable10);
        

    }
}
